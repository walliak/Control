void Delayms(unsigned int time)
{
	int i;
	while(time--)
	for(i=0;i<=20;i++);
}

void WrateC(unsigned char com)				//发送命令	由LCDWrite等函数调用

{
    unsigned char i,j,k,a,b;
    ClrLCDRS;  
    for(i=0;i<8;i++)
    { 
      a = P2OUT&0x20;
      b = (com&0x80)/4;
      j=com;	 			 
      if(a != b)	 	                                //移植修改部分*****
	P2OUT = P2OUT ^ 0x20;  
      P2OUT &= ~(0x10);
      for(k = 0;k < 3;k ++);//延时
      P2OUT |= (0x10);	
      for(k = 0;k < 3;k ++);//延时     
      com=j*2;                                                  //移植修改部分*****，相当于左移一位
     }
    SetLCDRS;
}

void WrateD(unsigned char dat)			  //发送数据	 由LCDWrite等函数调用

{
    unsigned char i,j,k,a,b;
	    SetLCDRS;  
	for(i=0;i<8;i++)
	{ 
          a = P2OUT&0x20;
          b = (dat&0x80)/4;
          j=dat;	 			 
           if(a != b)	 	                //移植修改部分*****
            P2OUT = P2OUT ^ 0x20;  
	  P2OUT &= ~(0x10);
          for(k = 0;k < 3;k ++);//延时
            P2OUT |= (0x10);	  
          for(k = 0;k < 3;k ++);//延时   
            dat=j*2;
        }
        ClrLCDRS;
}

void LCDWrite(unsigned char type,unsigned char dat)		//	#define		W_CMD		1		#define		W_DAT		0		向LCD传送信息 命令/数据	

{ 

	if(type)
	{
		WrateC(dat);
	}
	else
	{
		WrateD(dat);
	}
}

